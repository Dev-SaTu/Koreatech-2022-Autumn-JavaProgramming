package lib;

public abstract class Calculator {

	public abstract int add(int a, int b);
	public abstract int sub(int a, int b);
	public abstract int mult(int a, int b);
	public abstract double div(int a, int b);
	public abstract int abs_value(int x);
	public abstract int power(int x, int n);
	public abstract double avg(int[] a);
	
}
